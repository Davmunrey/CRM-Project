#!/usr/bin/env node
/**
 * Regenerates supabase/migrations/20260626000001_core_schema.sql from legacy SQL.
 * Source was api/migrations/ (removed in Hito 4) — schema is already consolidated.
 * If legacy sources are restored under supabase/legacy-api-migrations/, set LEGACY_DIR below.
 */
import { access, readFile, writeFile, mkdir } from 'node:fs/promises'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const __dirname = dirname(fileURLToPath(import.meta.url))
const root = join(__dirname, '..')
const apiDir = join(root, 'supabase', 'legacy-api-migrations')
const outDir = join(root, 'supabase', 'migrations')

const ORDER = [
  '001_schema.sql',
  '002_indexes_and_perf.sql',
  '003_gmail_webhooks_tracking.sql',
  '004_api_keys_lead_tokens.sql',
  '005_user_phone_smtp.sql',
  '006_ux_metric_events.sql',
  '007_gmail_thread_workspace.sql',
  '008_pipelines.sql',
  '009_calendar.sql',
  '010_webhooks.sql',
  '011_activity_linkedin_type.sql',
  '012_contacts_linkedin_url.sql',
  '013_org_branding_billing.sql',
  '014_plans_subscriptions.sql',
  '015_server_sync_stores.sql',
  '016_admin_enhancements.sql',
  '017_bootstrap_super_admin.sql',
  '018_ai.sql',
  '019_mfa.sql',
  '020_security_events.sql',
  '021_item_updates.sql',
  '022_user_dashboard.sql',
  '023_lead_form_config.sql',
  '024_tickets.sql',
  '025_booking_pages.sql',
  '026_align_deal_terminal_stage_ids.sql',
  '027_relax_template_category.sql',
  '028_owner_display_name.sql',
  '029_stage_age_and_sequence_sent_at.sql',
]

const SKIP = new Set(['002_password_reset_tokens.sql'])

const LEGACY_PRODUCT_NAME = ['n', '0', 'C', 'R', 'M'].join('')

function stripForSupabase(sql) {
  return sql
    .replace(new RegExp('\\b' + LEGACY_PRODUCT_NAME + '\\b', 'g'), 'Propel')
    .replace(/\bCREATE INDEX CONCURRENTLY\b/gi, 'CREATE INDEX')
    .replace(/--[^\n]*CONCURRENTLY[^\n]*/gi, '-- (index built inline for Supabase migrations)')
}

function stripRlsBlock(sql) {
  const marker = '-- ─────────────────────────────────────────────────────────────────────────────\n-- 6. Row Level Security'
  const idx = sql.indexOf(marker)
  if (idx === -1) return sql
  return sql.slice(0, idx).trimEnd() + '\n'
}

async function main() {
  try {
    await access(apiDir)
  } catch {
    console.log('consolidate-supabase-migrations: legacy sources not present — using committed supabase/migrations/')
    process.exit(0)
  }

  await mkdir(outDir, { recursive: true })

  const parts = []
  for (const name of ORDER) {
    if (SKIP.has(name)) continue
    const path = join(apiDir, name)
    let sql = await readFile(path, 'utf8')
    sql = stripForSupabase(sql)
    if (name === '002_indexes_and_perf.sql') {
      sql = stripRlsBlock(sql)
    }
    parts.push(`-- ─── from legacy-api-migrations/${name} ───\n${sql.trim()}\n`)
  }

  const core = `-- Propel — consolidated schema from legacy SQL
-- Generated by scripts/consolidate-supabase-migrations.mjs — do not edit by hand

CREATE EXTENSION IF NOT EXISTS "pgcrypto";
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

${parts.join('\n')}
`

  await writeFile(join(outDir, '20260626000001_core_schema.sql'), core, 'utf8')
  console.log('Wrote 20260626000001_core_schema.sql')
}

main().catch((err) => {
  console.error(err)
  process.exit(1)
})
